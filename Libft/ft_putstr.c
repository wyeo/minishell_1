#include "libft.h"

int		ft_putstr(char const *str)
{
	int		i;

	i = 0;
	if (str == NULL)
	{
		ft_putstr("NULL");
		return (-1);
	}
	while (str[i] != '\0')
	{
		ft_putchar(str[i]);
		i = i + 1;
	}
	return (0);
}
