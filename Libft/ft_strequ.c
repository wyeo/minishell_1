#include "libft.h"

/*
**	DESCRIPTION
**	The ft_strequ() function lexicographically compares the null-terminated
**	strings s1 and s2. It returns one if the strings are equal, otherwise it
**	returns zero.
*/

int	ft_strequ(char const *s1, char const *s2)
{
	if (s1 && s2)
	{
		if (!ft_strcmp(s1, s2))
			return (1);
	}
	return (0);
}
